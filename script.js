var side = 30
var grassArr = []
var grEaterArr = []
var predatorArr = []
var predatorEaterArr = []
var lavaArr = []


function setup() {
    createCanvas(side * matrix[0].length + 1, side * matrix.length + 1);
    background("gray")
    frameRate(3)

   for (var y = 0; y < matrix.length; y++) {
        for (var x = 0; x < matrix[y].length; x++) {
            if(matrix[y][x] == 1) {
                let gr = new Grass(x,y)
                grassArr.push(gr)
            }
          else if(matrix[y][x] == 2) {
                let grEater = new GrassEater(x,y)
                grEaterArr.push(grEater)
            }
         else if(matrix[y][x] == 3) {
                let Pr = new Predator(x,y)
                predatorArr.push(Pr)
            }
           else if(matrix[y][x] == 4) {
                let PrEater = new PredatorEater(x,y)
                predatorEaterArr.push(PrEater)
            }
           else if(matrix[y][x] == 5) {
                let lava1 = new Lava(x,y)
                lavaArr.push(lava1)
            }
        }
    }

}



function draw() {
    fill("grey")
    for (let y = 0; y < matrix.length; y++) {
        for (let x = 0; x < matrix[0].length; x++) {
            if (matrix[y][x] == 1) {
                fill("green")
            } else if (matrix[y][x] == 2) {
                fill("yellow")
            } else if (matrix[y][x] == 3) {
                fill("red")
            } else if (matrix[y][x] == 4) {
                fill("blue")
            } else if (matrix[y][x] == 5) {
                fill("orange")
            } else {
                fill("gray")
            }
            rect(x * side, y * side, side, side)
        }
    }

   

    for (let i in grassArr) {
        grassArr[i].mul()
    }

    for (let i in grEaterArr) {
        grEaterArr[i].start()
    }
    for (let i in predatorArr) {
        predatorArr[i].start()
    }
    for(let i in predatorEaterArr) {
      predatorEaterArr[i].start()
    }
    for(let i in lavaArr) {
      lavaArr[i].start()
    }
   


}



